# The equality operator == can be used to check if an array
# is equal to another array or not:
p [1,2,3] == [1,2,3]                                              # => true
p [1,2] == [0]                                                    # => false

# The inequality operator != does the opposite, it returns the
# result opposite to the == operator
p [1,2,3] != [1,2,3]                                              # => false
p [1,2] != [0]                                                    # => true
