# PART 3                                        
# The greater than operator can be used         
# to check if a String or Integer is greater    
# than others                                   
p 5 > 4                                         # => true
p 6 > 7                                         # => false
p 'a' > 'b'                                     # => false
p 'b' > 'a'                                     # => true
                                                
p 'A'.ord, 'a'.ord                              # => [65, 97]
p 'a'.ord > 'A'.ord                             # => true
