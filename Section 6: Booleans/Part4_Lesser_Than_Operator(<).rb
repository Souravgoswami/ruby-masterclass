# PART 4                                                   
# Just like the >, the lesser than operator can be         
# used to check if a String or an Integer object is        
# greater than it's argument                               
                                                           
p 1 < 5                                                    # => true
p 'a' < 'b'                                                # => true
p STDIN.gets.to_i < 5                                      # => true
